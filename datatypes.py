# Checking data types

print(type(10))          # Int
print(type('Aanchal'))          # String
print(type([1, 2, 3]))        # List
print(type({'name':'Aanchal'})) # Dictionary
print(type(['Aanchal', 'Aaditi', 'Aakashi']))   # Set
print(type((9.8, 3.14, 2.7)))    # Tuple
print(type(4-2j))      # Complex number