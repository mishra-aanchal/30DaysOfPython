# Find area of a circle
# Find circumfrence of a circle

r =30
pi = 3.14
area_of_circle = 2 * pi * r
circum_of_circle = pi * r**2;
print("Area of circle is:", area_of_circle)
print("Circumfrence of circle is:", circum_of_circle)
#convert area and circumfrence to int data type
area_of_circle_int = int(area_of_circle)
circum_of_circle_int = int(circum_of_circle)
print(area_of_circle_int);
print(circum_of_circle_int)